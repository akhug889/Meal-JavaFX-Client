package model;

public class Ingredient {

    // Attributes
    private String name;

    // Constructor

    public Ingredient() {
    }

    public Ingredient(String name) {
        this.name = name;
    }

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

